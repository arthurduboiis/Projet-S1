<script>
const ratio = .1;

const options = {
root: null,
rootMargin: '0px', // marge pour que ce soir visible
threshold: ratio // permet d'afficher lorsque 10% est visible
}

const handleIntersect = function (entries, observer) {
entries.forEach(function (entry){
    // Si ce qu'on voit avec intersectionRatio et supérieur au 10% demander
    // grâce au ratio alors on passe la classe en visible
    if (entry.intersectionRatio > ratio) {
        entry.target.classList.add('reveal-visible')
        observer.unobserve(entry.target)
    }
})
}

const observer = new IntersectionObserver(handleIntersect, options)
document.querySelectorAll('.reveal').forEach(function(r){
observer.observe(r)
})
</script>


